print(num1 + num2 + num3)

#Calculate the sum of the nrs
sum_numbers = num1 + num2 + num3
print(sum_numbers)

#Calculate first nr minus second nr
subtraction_numbers = num1 - num2
print(subtraction_numbers)

#Calculate third nr multiplied by first nr
multi_numbers = num1 * num3
print(multi_numbers)

#Calculate sum of all three nr sdivided by third nr
division_numbers = sum_numbers / num3 
